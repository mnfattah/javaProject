package thirdpackage;

public class DoWhileLoop {

    public static void main(String[] args) {
        //dowhile loop
        int x = 0;
        do {
            System.out.println("My name is " + x);
            x++;
        } while (x < 10);

        //while loop
        int y = 0;
        while (y < 5) {
            System.out.println("Java" + y);
            y++;
        }


    }
}




